CREATE TABLE TB_PERMISSION
(
	PERMISSION_ID		CHAR(32)		NOT NULL,
	RESOURCE_ID			CHAR(32)		NOT NULL,
	ROLE_ID				CHAR(32)		NOT NULL,
	CONSTRAINT	PK_TB_PERMISSION PRIMARY KEY (PERMISSION_ID),
	CONSTRAINT	FK_PERMISSION_RESOURCE FOREIGN KEY (RESOURCE_ID) REFERENCES TB_RESOURCE(RESOURCE_ID),
	CONSTRAINT	FK_PERMISSION_ROLE FOREIGN KEY (ROLE_ID) REFERENCES TB_ROLE(ROLE_ID)
);