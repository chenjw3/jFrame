CREATE TABLE TB_RESOURCE
(
	RESOURCE_ID					CHAR(32) 				NOT NULL,
	RESOURCE_CODE				VARCHAR(50)				NOT NULL,
	RESOURCE_NAME				VARCHAR(200)			NOT NULL,
	RESOURCE_URL				VARCHAR(200),
	RESOURCE_TYPE_CODE			VARCHAR(10)				NOT NULL,
	PARENT_RESOURCE_CODE		VARCHAR(50)				NOT NULL,
	PROJECT_CODE				VARCHAR(10)				NOT NULL,
	DEPTH						INT 					NOT NULL,
	RESOURCE_ICON				VARCHAR(50),
	ACTIVE						CHAR(1)					DEFAULT '1' ,
	CONSTRAINT PK_TB_RESOURCE PRIMARY KEY (RESOURCE_ID)
);