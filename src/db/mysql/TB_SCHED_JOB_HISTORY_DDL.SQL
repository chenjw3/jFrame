CREATE TABLE TB_SCHED_JOB_HISTORY
(
	HISTORY_ID				CHAR(32)				NOT NULL,
	JOB_RUN_TIME			TIMESTAMP				NOT NULL,
	JOB_ID					CHAR(32)				NOT NULL,
	JOB_DATA				BLOB,
	CONSTRAINT PK_TB_SCHED_JOB_HISTORY  PRIMARY KEY(HISTORY_ID),
	CONSTRAINT FK_HISTORY_JOB_ID FOREIGN KEY (JOB_ID) REFERENCES TB_SCHED_JOB (JOB_ID)
);