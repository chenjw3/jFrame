CREATE TABLE TB_CODE_LIST
(
	CODE_LIST_ID			CHAR(32) 				NOT NULL,
	PARENT_ID				VARCHAR(50),
	CODE_KEY				VARCHAR(50)				NOT NULL,
	CODE_VALUE				VARCHAR(200)			NOT NULL,
	GROUP_NAME				VARCHAR(200),
	DEPTH					INT 					NOT NULL,
	SORT_ORDER				INT,
	ACTIVE					CHAR(1)					DEFAULT '1' ,
	CONSTRAINT TB_CODE_LIST PRIMARY KEY (CODE_LIST_ID)
);